	<p>Paharpur an important archaeological site in Bangladesh, situated in a village named Paharpur (Pahadpur) under the Badalgachhi Upazila of Naogaon district. The village is connected with the nearby Railway station Jamalganj, the district town Naogaon and Jaipurhat town by metalled roads. It is in the midst of alluvial flat plain of northern Bangladesh. In contrast to the monotonous level of the plain, stands the ruins of the lofty (about 24m high from the surrounding level) ancient temple which was covered with jungle, locally called Pahar or hill from which the palace got the name of Paharpur.

The site was first noticed by Buchanon Hamilton in course of his survey in Eastern India between 1807 and 1812. Westmacott next visited it. Sir Alexander Cunningham visited the place in 1879. Cunningham intended to carry out an extensive excavation in the mound. But he was prevented by zamindar of Balihar, the owner of the land. So he had to be satisfied with limited excavation in a small part of the monastic area and top of the central mound. In the latter area he 'discovered the ruins of a square tower of 6.70m (22 ft) side with a projection in the middle of each side'. The site was declared to be protected by the Archaeological Survey of India in 1919 under the Ancient Monuments Preservation Act of 1904. </p>
