	<p>The Mosque City of Bagerhat is a formerly lost city, located in the suburbs of Bagerhat city in Bagerhat District, in the Khulna Division of southwest of Bangladesh. Bagerhat is about 15 miles south east of Khulna and 200 miles southwest of Dhaka.[1]

    Originally known as Khalifatabad and nicknamed the "mint town of the Bengal Sultanate",[2] the city was founded in the 15th century by the warrior saint Turkish general Ulugh Khan Jahan.[3]

    The historic city, listed by Forbes as one of the 15 lost cities of the world, has more than 50 Islamic monuments in the distinct Bengal variant of Indo-Islamic architecture. These were uncovered after removing the vegetation that had obscured them from view for many centuries. The site has been recognised as a UNESCO World Heritage Site in 1983 under criteria (iv), "as an outstanding example of an architectural ensemble which illustrates a significant stage in human history",[4] of which the Sixty Pillar Mosque (Shat Gombuj Masjid in Bengali), constructed with 60 pillars and 77 domes, is the most well known.[2][5] Apart from these monuments, UNESCO also includes the mausoleum of Khan Jahan, the mosques of Singar, Bibi Begni, Reza Khoda, Zindavir among the unique monuments.[4][6]<p>
	
